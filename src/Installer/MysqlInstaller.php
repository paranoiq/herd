    public function translatePort(int $port, Version $version): int
    {
        // 5.7.44 -> 35744
        // 8.0.11 -> 38011

        return intval('3' . $version->major . $version->minor . str_pad(strval($version->patch), 2, '0', STR_PAD_LEFT));
    }

    public function loadReleaseNotesListsUrls(): void
    {
        $baseUrl = 'https://dev.mysql.com/';

        $this->releaseNotesListsUrls = [
            "9.6" => "https://dev.mysql.com/doc/relnotes/mysql/9.6/en/", // seed
        ];

        $releaseNotesListRe = '~doc/relnotes/mysql/(\d+\.\d+)/en/~i';
        foreach ($this->releaseNotesListsUrls as $fam => $releasesUrl) {
            $fam = str_replace('.', '-', $fam);
            $cache = new FileInfo("{$this->baseDir}/cache/{$this->dir}/release-notes-{$fam}.html");
            if ($cache->exists() && $cache->getModifiedTime()->isAfter(new DateTime('-1 day'))) {
                $html = $cache->read();
            } else {
                $this->console->writeLn("Refreshing release notes ({$releasesUrl})");

                $request = $this->httpHelper->createHttpRequest($releasesUrl);
                $response = $request->execute();
                $html = $response->getBody();
                $cache->write($html);
            }

            foreach (Re::matchAll($html, $releaseNotesListRe, PREG_SET_ORDER) as $match) {
                $this->releaseNotesListsUrls[$match[1]] = $baseUrl . $match[0];
            }
        }

        // inject versions that are no longer in the web release notes
        foreach ($this->releaseDates as $ver => $date) {
            [$major, $minor, $patch] = Parse::ints($ver, '.');
            $version = Version::new3($major, $minor, $patch, null, null, new Date($date), $this->fancyName);
            if (!isset($this->minVersion) || version_compare($this->versionKey($version), $this->minVersion) >= 0) {
                $this->remote[$this->familyKey($version)][$this->versionKey($version)] = $version;
            }
        }
    }

